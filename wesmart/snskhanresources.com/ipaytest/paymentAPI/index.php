<h2>Example code</h2>
<ul>
    <li><code>functions.php</code>: Helper functions to generate signature string and call requests to WPC service.</li>
    <li><code>initiate.php</code>: Initiate the payment.</li>
    <li><code>return.php</code>: Get transaction details (return callback url).</li>
    <li><code>cancel.php</code>: The buyer cancel the payment (cancel call back url).</li>
</ul>

<h2>Get started</h2>
<p>Open files <code>initiate.php</code> and <code>return.php</code> and change your the config by your merchant config values</p>
<p>Then, run the demo to understand the payment flow.</p>

